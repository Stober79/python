numbers = (50,100,150,200,250) # krotka z liczbami
print(type(numbers)) # wyświetla typ danych - tuple
print(len(numbers)) # wyświetla długość krotki
print(numbers[len(numbers)-1]) # wyświetla ostatni element krotki
print(numbers[1:4]) # slicing - wyświetla elementy od indeksu 1 do 3
print(numbers[-3]) # slicing - wyświetla trzy ostatnie elementy krotki