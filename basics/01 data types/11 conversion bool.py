#Falsy values:wartości, które są interpretowane jako False w kontekście logicznym. Oto kilka przykładów falsy values w Pythonie:
print(bool(False))
print(bool(0))
print(bool(0.0))
print(bool(""))
print(bool([]))
print(bool(()))
print(bool({}))
print(bool(None))

#takie które dają True
print(bool(True))
print(bool(1))
print(bool(0.1))
print(bool("Hello"))
print(bool([1, 2, 3]))
print(bool((1, 2, 3)))
print(bool({1, 2, 3}))